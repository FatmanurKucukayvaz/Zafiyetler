<h1>Windows'ta Firefox Kurulumu</h1>
<p>Öncelike Firefox indirme sayfasını herhangi bir tarayıcıda (mesela Microsoft Internet Explorer) ziyaret edin. Sayfa, otomatik olarak işletim sisteminizi ve dilinizi tanıyarak size en uygun Firefox versiyonunu önerecektir. Eğer Firefox'un başka bir sürümünü indirmek istiyorsanız, sağ taraftaki menüden "Diğer Sistemler ve Diller"'e tıklayarak, kullanılabilir diğer sürümleri görebilirsiniz.</p>
<div style="margin-top:7px; margin-left:150px"><img src="moz1.jpg" align="right"/></div>
